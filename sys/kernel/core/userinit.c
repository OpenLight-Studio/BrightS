/*
 * Embedded ELF64 init program for BrightS.
 *
 * This is a minimal user-mode program that:
 *   1. Writes a banner to stdout
 *   2. Enters a read-eval loop reading from stdin
 *   3. Supports basic commands: help, echo <text>, exit
 *   4. On unrecognized input, prints "unknown cmd"
 *
 * The program uses syscall interface:
 *   #2 = write(fd, buf, len)
 *   #1 = read(fd, buf, len)
 *   #5 = exit(status)
 *   #11 = sleep_ms(ms)
 */

/*
 * ELF binary layout:
 *   0x0000 - 0x003F : ELF header (64 bytes)
 *   0x0040 - 0x0077 : Program header (56 bytes)
 *   0x0078          : Entry point (_start)
 *
 * Virtual base: 0x400000
 *
 * Data section (after code):
 *   banner string, prompt string, help text, command strings
 *
 * The shell loop:
 *   - write prompt
 *   - read line into buffer
 *   - compare with known commands
 *   - dispatch
 */

/* Total ELF size: ~512 bytes should be enough for a minimal shell */
